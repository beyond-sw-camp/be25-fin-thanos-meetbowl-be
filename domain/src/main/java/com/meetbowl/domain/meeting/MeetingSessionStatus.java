package com.meetbowl.domain.meeting;

/**
 * 회의 진행 세션이 생성된 뒤 종료될 때까지의 생명주기를 나타낸다.
 *
 * <p>외부 공급자의 일시적인 연결 상태를 그대로 복제하는 값이 아니라, Meetbowl이 회의 입장 허용과 종료 여부를 판단할 때 사용하는 업무 상태다.
 */
public enum MeetingSessionStatus {
    /** 공급자 회의방은 준비됐지만 실제 회의 진행은 시작되지 않은 상태다. */
    READY,

    /** 참가자가 입장할 수 있고 회의가 진행 중인 상태다. */
    ACTIVE,

    /** 신규 입장을 막고 세션 종료 처리를 수행 중인 전이 상태다. */
    ENDING,

    /** 회의 종료 처리가 완료되어 더 이상 사용하지 않는 상태다. */
    ENDED,

    /** 공급자 연동 또는 내부 처리 오류로 정상 진행할 수 없는 상태다. */
    FAILED
}
