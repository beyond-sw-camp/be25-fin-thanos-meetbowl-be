package com.meetbowl.domain.meeting;

/**
 * 회의에 접속한 참가자의 인증 주체와 회의 내 역할을 구분한다.
 *
 * <p>회원 기반 참가자는 사용자 ID를 사용하고, 게스트는 초대/입장 권한 검증 결과로 받은 게스트 식별자를 사용한다. 이 구분을 통해 게스트가 회원 전용 기능으로 확장되는
 * 것을 방지한다.
 */
public enum ParticipantType {
    /** 회의를 주최하고 진행 권한을 가진 회원 참가자다. */
    HOST,

    /** 초대되거나 참석 대상으로 등록된 일반 회원 참가자다. */
    PARTICIPANT,

    /** 회의 참여 권한만 부여받은 비회원 참가자다. */
    GUEST
}
